				<li>Custom online courses -- $2,500/course</li>
			</ol>
		</li>
		<li>
			Course Development:
			<ol>
				<li>$100/hour for instructional design and course conversion</li>
			</ol>
		</li>
		<li>
			Full eSchool Management:
			<ol>
				<li>
					Fees:
					<ol>
						<li>Nonprofits = $3,000/year + $2.00/user/year</li>
						<li>For-Profits =  $5000/year + $4.00/user/year</li>
					</ol>
				</li>
				<li>
					Services:
					<ol>
						<li>Bulk uploading of students and teachers</li>
