	<h5>The Global Classroom eSchool Sponsorship Program</h5>
	<p>
		Terms of Use for Schools Accepting Sponsorships
	</p>
	<h5>Program Goals</h5>
	<p>
		This unique program, organized and managed by Global Classroom, is intended as an optional feature for
		all eSchools on the Global Classroom network. It creates a process for accepting local and national
		sponsors to fund digital classroom hosting, teacher training, content development, eSchool management,
		and teacher incentive programs. As part of every school’s mission to raise student performance, the goal
		of every eSchool is to create 100% adoption by teachers to raise student performance in the classroom.
	</p>
	<p>
		The Global Classroom eSchool Sponsorship Program is intended as neither advertising nor charitable giving.
		Rather it is a way to promote 21st century education by helping Global Classroom defray the costs of its
		services for schools and other learning organizations. Therefore, proceeds given to the Global Classroom
		eSchool Sponsorship program cannot be considered charitable gifts for taxation purposes. Though this is
		not advertising, Global Classroom has included the ability for sponsors to offer special discounts to
		teachers and students on their profile page to support education.
	</p>
	<h5>Sponsorship Recognition</h5>
	<p>
		eSchool sponsors are displayed in a special sponsorship section for students, parents, teachers and
		administrators that can be accessed by clicking the “Sponsors” tab on the user and eSchool homepages, as
		well as on the homepage of the Global Classroom website.
	</p>
	<h5>Sponsorship Levels</h5>
	<ul>
		<li><em>Platinum Sponsors</em> - $100 per month or $1,000 per year</li>
		<li><em>Gold Sponsors</em> - $50 per month or $500 per year</li>
		<li><em>Silver Sponsor</em> - $10 per month or $100 per year</li>
	</ul>
	<h5>Use of Funds</h5>
	<p>
		All sponsorship funds received on behalf of an eSchool* will be used to defray the published costs of
		services provided by Global Classroom. To be authorized by Global Classroom to receive sponsorships, each
		eSchool must agree to have unfunded monthly subscriptions covered by sponsorship revenues. Current eSchool
		monthly subscription rates (calculated by the year) are:
	</p>
	<ol>
		<li>Nonprofits = $29.95/month for the first ten (10) instructors and $2.00/instructor/month beyond ten instructors</li>
		<li>For-Profits =  $49.95/month for the first ten (10) instructors and $4.00/instructor/month beyond ten instructors</li>
	</ol>
