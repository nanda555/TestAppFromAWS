	<p>
		One hundred percent (100%) of all sponsorship funds will be used to cover these costs if they are not
		already covered by the eSchool*. If sponsorship revenues exceed actual eSchool costs, as authorized
		by the sponsored eSchools, then eighty percent (80%) of the remaining revenues will be delivered to
		the eSchools and twenty percent (20%) will be retained by Global Classroom to help improve functionality
		of the network and digital classroom technology.
	</p>
